    private void processPayment() {
        try {
            String paymentMethod = jRadioButton1.isSelected() ? "CASH" : "PAYWAVE";
            double changeAmount = 0.0;
            
            if (jRadioButton1.isSelected()) {
                try {
                    double cashReceived = Double.parseDouble(jTextField1.getText());
                    if (cashReceived < totalAmount) {
                        JOptionPane.showMessageDialog(this, 
                            "Insufficient cash received. Please enter at least " + currencyFormat.format(totalAmount),
                            "Payment Error", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    changeAmount = cashReceived - totalAmount;
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, 
                        "Please enter a valid cash amount.",
                        "Payment Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            
            Payment payment = new Payment(orderId, totalAmount, paymentMethod);
            payment.setChangeAmount(changeAmount);
            payment.setStatus("COMPLETED");
            
            processedPayment = paymentDAO.createPayment(payment);
            
            if (callback != null) {
                callback.onPaymentComplete(processedPayment);
            }
            
            dispose();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, 
                "Error processing payment: " + e.getMessage(),
                "Database Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Unexpected error: " + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

