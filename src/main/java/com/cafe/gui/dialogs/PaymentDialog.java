    private void setupOrderDetails() {
        jLabel2.setText(currencyFormat.format(totalAmount));
        updateCashFieldVisibility();
    }
    
    private void updateCashFieldVisibility() {
        boolean isCash = jRadioButton1.isSelected();
        jLabel4.setVisible(isCash);
        jTextField1.setVisible(isCash);
        jLabel5.setVisible(isCash);
        jLabel6.setVisible(isCash);
        
        if (!isCash) {
            jLabel6.setText("$0.00");
        }
    }
    
    private void calculateChange() {
        if (jRadioButton1.isSelected()) {
            try {
                double cashReceived = Double.parseDouble(jTextField1.getText());
                double change = cashReceived - totalAmount;
                jLabel6.setText(currencyFormat.format(Math.max(0, change)));
            } catch (NumberFormatException e) {
                jLabel6.setText("$0.00");
            }
        }
    }
    
    public Payment getProcessedPayment() {
        return processedPayment;
    }

